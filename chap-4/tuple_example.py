# tuple example and method 

# count(value),index(value),len(tuple),min(tuple),max(tuple)

tuple_example = (1, 2, 3, 4, 5)
print(tuple_example[0])
print(tuple_example[1])
print(tuple_example[3])
print(tuple_example[4])


tuple1 = (1, 2, 3, 4, 5)
print(tuple1)  # Output: (1, 2, 3, 4, 5)

tuple2 = (1, 2, 3, 4, 5)
print(tuple2.count(3))  # Output: 1

tuple3 = (1, 2, 3, 4, 5)
print(tuple3.index(3))  # Output: 2

tuple3 = (1, 2, 3, 4, 5)
print(len(tuple3))  # Output: 5

tuple4 = (1, 2, 3, 4, 5)
print(min(tuple4))  # Output: 1

tuple5 = (1, 2, 3, 4, 5)
print(max(tuple5))  # Output: 5




