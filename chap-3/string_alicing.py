# synatx variable[start_index:end _index]


# note the tsring is immutable in python

name ='dhananjaya'
print(name[2:4])  #an
print(name[2:])  #anajaya
print(name[:4])  #dhan
print(name[:])  #dhananjaya


# slicing with step size
# syntax variable[start_index:end_index:step_size]
# note the step size is optional and default is 1

name ="dhananjaya"
print(name[2:4:2])
# a
