# string methode
# len(), capitalize(), upper(), lower(), find(),replace()  ,startswith(),endswith()......etc

name = "dhananjaya"
print(name.capitalize()) #Dhananjaya
print(name.upper()) #DHANANJAYA
print(name.lower()) #dhananjaya
print(name.find("a")) #2
print(name.replace("n", "N")) #DhAnaNjAyA
print(name.startswith("Dha")) #FALSE
print(name.endswith("ya")) #TRUE